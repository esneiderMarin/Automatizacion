package com.viajesexito.certification.purchase.model;

public class ViajesExitoModel {

	public  String originCity;
	public  String destinationCity;
	public String DateOrigin;
	public String DateBack;
	
	public ViajesExitoModel() {
	}

	public String getOriginCity() {
		return originCity;
	}

	public void setOriginCity(String originCity) {
		this.originCity = originCity;
	}

	public String getDestinationCity() {
		return destinationCity;
	}

	public void setDestinationCity(String destinationCity) {
		this.destinationCity = destinationCity;
	}

	public String getDateOrigin() {
		return DateOrigin;
	}

	public void setDateOrigin(String dateOrigin) {
		DateOrigin = dateOrigin;
	}

	public String getDateBack() {
		return DateBack;
	}

	public void setDateBack(String dateBack) {
		DateBack = dateBack;
	}
	
	
}
